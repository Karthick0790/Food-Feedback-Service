package com.foodfeed;

public class FeedbackInput {

	private String foodName;
	private String feed;

	void setFoodName(String foodName) {	
		this.foodName = foodName;
	}
	
	String getFoodName() {
		return foodName;
	}
	
	void setFeed(String feed) {	
		this.feed = feed;
	}
	
	String getFeed() {
		return feed;
	}
}
